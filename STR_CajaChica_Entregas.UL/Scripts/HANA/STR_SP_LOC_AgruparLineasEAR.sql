CREATE PROCEDURE STR_SP_LOC_AgruparLineasEAR
(
	DOCENT INT,
	CODPRV VARCHAR(20),
	TPODOC VARCHAR(20),
	SREDOC VARCHAR(20),
	CORDOC VARCHAR(50)
)
AS
	LINEAS VARCHAR(1000);
BEGIN
	SELECT STRING_AGG("LineId",',') INTO LINEAS FROM "@STR_EARCRGDET" T0 INNER JOIN "@STR_EARCRG" T1 ON T0."DocEntry" = T1."DocEntry"
	WHERE T1."DocEntry" = :DOCENT AND "U_ER_CDPV" = :CODPRV AND "U_ER_TDOC" = :TPODOC  AND "U_ER_SDOC" = :SREDOC  AND "U_ER_CDOC" = :CORDOC AND "U_ER_SLCC" = 'Y';
	SELECT :LINEAS FROM DUMMY;
END
