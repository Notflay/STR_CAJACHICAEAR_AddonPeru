CREATE PROCEDURE STR_SP_ActualizarDatosPorReaperturaCCH
	@NMBCCH VARCHAR(50),
	@NUMCCH VARCHAR(50)
AS
BEGIN
	UPDATE T0 SET T0.U_CC_STDO = 'C' FROM [@STR_CCHAPRDET] T0 INNER JOIN [@STR_CCHAPRDET] T1  
	ON T0.U_CC_NMCC = T1.U_CC_NCCR WHERE T0.U_CC_CJCH = @NMBCCH and T0.U_CC_NMCC = @NUMCCH
	UPDATE T1 SET T1.U_CC_NMCC = T1.U_CC_NCCR FROM [@STR_CCHAPRDET] T0 INNER JOIN [@STR_CCHAPRDET] T1  
	ON T0.U_CC_NMCC = T1.U_CC_NCCR WHERE T0.U_CC_CJCH = @NMBCCH and T0.U_CC_NMCC = @NUMCCH
END